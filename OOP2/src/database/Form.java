 private void addInfo(String gender) { //add data to database
       
     process.executeUpdate("Insert into `my_table` values(NULL,'"+fname.getText()+"','"+lname.getText()+"','"+address.getText()+"','"+gender+"') ");
         setTable();
       
    }
 private void updateInfo(String gender,int id) { // update data in database
      process.executeUpdate("UPDATE `my_table` SET `fname`='"+fname.getText()+"',`lname`='"+lname.getText()+"',`address`='"+address.getText()+"',`gender`='"+gender+"' WHERE  `id`='"+id+"'");
         setTable();
    }
 private void setTable() { //set data from database to prooject table
    tablerow =process.getAllRecord("Select * from `my_table`");
    model = new DefaultTableModel(tablerow,tablecol);
    Rtable.setModel(model); 
    }
 
 private int getRowNumber() { // get id from selected table

     return Integer.parseInt(model.getValueAt(Rtable.getSelectedRow(),0).toString());

 }
  private void setDataToTextFields(int id) { // get specific value in database and set to textfields
     //To change body of generated methods, choose Tools | Templates.
    fname.setText(process.getSpecificField("Select `fname` from `my_table` where `id`='"+id+"'"));
    lname.setText(process.getSpecificField("Select `lname` from `my_table` where `id`='"+id+"'"));
    address.setText(process.getSpecificField("Select `address` from `my_table` where `id` ='"+id+"'"));
    String g=process.getSpecificField("Select `gender` from `my_table` where `id` = '"+id+"'");
   
    if(g.equals("Male")){
        male.setSelected(true);
        fmale.setSelected(false);
    }if(g.equals("Female")){
        fmale.setSelected(true);
        male.setSelected(false);
    }
}
  private void deleteInfo(int record_id){//deleting record from database
      process.executeUpdate("DELETE FROM `my_table` where `id`='"+record_id+"' ");
      setTable();
      
  }

   
