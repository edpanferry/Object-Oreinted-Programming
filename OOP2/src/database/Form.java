    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
       // TODO add your handling code here:
        String gen=null;
        if(male.isSelected()){
            gen="Male";
        }if(fmale.isSelected()){
            gen="Female";
        }
        int x=0;
        if(gen==null){
            gender_error.setText("Gender is required.");
            x++;
        }
        if(fname.getText().isEmpty()){
            fname_error.setText("Firstname is required.");
            x++;
        }
        if (lname.getText().isEmpty()){
            lname_error.setText("Lastname is required.");
            x++;
        }
        if(address.getText().isEmpty()){
            
            address_error.setText("Address is required");
            x++;
        }
       
            if(x==0) {
        addInfo(gen);
        edit.setEnabled(false); 
        delete_btn.setEnabled(false);
        clearField();
        address_error.setText("");
         lname_error.setText("");
          gender_error.setText("");
           fname_error.setText("");
        }
    }//GEN-LAST:event_saveActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
         String gen=null;
        if(male.isSelected()){
            gen="Male";
        }if(fmale.isSelected()){
            gen="Female";
        }
        int id = getRowNumber();
        if(fname.getText().trim().isEmpty()||lname.getText().trim().isEmpty()||address.getText().trim().isEmpty()||gen!="Male"&&gen!="Female"){
           JOptionPane.showMessageDialog(null, "Please Complete the Form");
       }
        else{
            updateInfo(gen,id);
            clearField();
            edit.setEnabled(false);
            delete_btn.setEnabled(false);
            
        }
    }//GEN-LAST:event_editActionPerformed

